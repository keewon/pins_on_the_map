    headers = {"Authorization": f"KakaoAK {API_KEY}"}
    params = {
        "query": query,
        "page": page,
        "size": 15,
    }
    
    response = requests.get(url, headers=headers, params=params)
    response.raise_for_status()
    return response.json()


